package emr.assessment.model;

public class Abdomen {
	private String feeling;
	private boolean distended;
	
	public String getFeeling() {
		return feeling;
	}
	public void setFeeling(String feeling) {
		this.feeling = feeling;
	}
	public boolean isDistended() {
		return distended;
	}
	public void setDistended(boolean distended) {
		this.distended = distended;
	}
}
